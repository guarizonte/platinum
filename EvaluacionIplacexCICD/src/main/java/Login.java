    private void checkLogin(ActionEvent evt) {
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/platinum", "root", "");

            String query = "SELECT * FROM usuarios WHERE username = ? AND password = ?";
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, username);
            statement.setString(2, password);

            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                JOptionPane.showMessageDialog(null, "Login exitoso", "Exito", JOptionPane.INFORMATION_MESSAGE);
                openIntranet(resultSet.getString("nombre"), resultSet.getString("apellido"));
            } else if ("".equals(password) || "".equals(username)) {
                JOptionPane.showMessageDialog(null, "Datos no pueden estar vacios", "Faltan Datos", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o Contraseña invalidos", "Error", JOptionPane.INFORMATION_MESSAGE);
            }

            resultSet.close();
            statement.close();
            connection.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Database connection error");
        }
    }
    
    private void openIntranet(String nombre, String apellido) {
        Intranet intranet = new Intranet(nombre, apellido);
        dispose();
        intranet.setVisible(true);
    }
    
